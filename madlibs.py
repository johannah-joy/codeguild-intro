astronomical_object = input("Name an astronomical object: ")
gem = input("Name a gem: ")
adjective = input("Type an adjective: ")
verb = input("Choose a verb: ")
noun = input("Use a noun: ")

print(f'''
Twinkle, twinkle, little {astronomical_object},
How I wonder what you are.
Up above the world so high,
Like a(n) {gem} in the sky.

When the {adjective} sun is gone,
When he nothing shines upon,
Then you {verb} your little light,
Twinkle, twinkle, all the night.

Then the {noun} in the dark,
Thanks you for your tiny spark,
He could not see which way to go,
If you did not twinkle so.
''')

